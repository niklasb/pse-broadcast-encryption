package cryptocast.client;

/**
 * This class is for showing the received traffics.
 *
 */
public class Traffics {
	
	/**
	 * The received traffics
	 */
	private long receivedTraffics;
	
	
	/**
	 * Constructor for this class
	 */
	public Traffics() { 
		
	}

	/**
	 * @return the receivedTraffics
	 */
	public long getReceivedTraffics() {
		return receivedTraffics;
	}

	/**
	 * @param receivedTraffics the receivedTraffics to set
	 */
	public void setReceivedTraffics(long receivedTraffics) {
		this.receivedTraffics = receivedTraffics;
	}
	
	
	
	

}
